public class WhileLoops {
    public static void main(String[] args) {
        boolean myBooleanTrue = 5 > 2;
        boolean myBooleanFalse = 5 < 2;

        System.out.println(myBooleanTrue);
        System.out.println(myBooleanFalse);

        int value = 0;

        while (value < 5){
            System.out.println(value);
            value++;

        }



    }

}
